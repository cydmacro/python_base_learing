# ============================================================
# 第三部分: 数据类型 - 认识不同种类的数据
# ============================================================

# ============ 知识点 ============
# 1. 整数(int): 没有小数点的数字
# 2. 浮点数(float): 有小数点的数字
# 3. 字符串(str): 用引号包裹的文本
# 4. 布尔值(bool): True或False

# ============ 实战技巧 ============
# 1. type()函数可以查看数据类型
# 2. Python会自动识别数据类型
# 3. 合理选择数据类型能避免错误

# ============ 易错点 ============
# 1. 数字和字符串不能直接相加
# 2. True/False首字母必须大写
# 3. 字符串"123"和整数123是不同的

# ============ 扩展思考 ============
# 1. 如何判断一个变量的类型?
# 2. 不同类型之间能相互转换吗?
# 3. 为什么需要不同的数据类型?

# 整数类型
student_count = 30
print("学生人数:", student_count)
print("类型:", type(student_count))

# 浮点数类型
average_score = 85.5
print("平均分:", average_score)
print("类型:", type(average_score))

# 字符串类型
class_name = "Python基础班"
print("班级:", class_name)
print("类型:", type(class_name))

# 布尔类型
is_passed = True
print("是否通过:", is_passed)
print("类型:", type(is_passed))


# ============================================================
# 第四部分: 类型转换 - 不同类型之间的转换
# ============================================================

# ============ 知识点 ============
# 1. int(): 转换为整数
# 2. float(): 转换为浮点数
# 3. str(): 转换为字符串

# ============ 实战技巧 ============
# 1. 字符串"123"可以转为整数123
# 2. 整数可以转为字符串用于拼接
# 3. 浮点数转整数会丢失小数部分(截断)

# ============ 易错点 ============
# 1. 字符串"abc"不能转为数字
# 2. 包含小数点的字符串"12.5"不能直接转int
# 3. 先转float再转int: int(float("12.5"))

# ============ 扩展思考 ============
# 1. 如何处理转换失败的情况?
# 2. 有没有更智能的转换方式?
# 3. 什么情况下需要类型转换?

# 字符串转数字
age_str = "20"
age_num = int(age_str)
print("字符串'20'转整数:", age_num, type(age_num))

score_str = "85.5"
score_num = float(score_str)
print("字符串'85.5'转浮点数:", score_num, type(score_num))

# 数字转字符串
count = 100
count_str = str(count)
print("整数100转字符串:", count_str, type(count_str))

# 浮点数转整数(截断)
pi = 3.14159
pi_int = int(pi)
print("浮点数3.14159转整数:", pi_int)  # 结果是3，小数部分被丢弃


# ============================================================
# 第五部分: 综合示例 - 个人信息展示
# ============================================================

# ============ 知识点 ============
# 1. 变量命名规范
# 2. 不同数据类型的使用
# 3. 格式化输出

# ============ 实战技巧 ============
# 1. 用有意义的变量名
# 2. 字符串拼接用+号
# 3. 数字需要转字符串才能拼接

# ============ 易错点 ============
# 1. 字符串和数字不能直接用+拼接
# 2. 需要用str()转换数字为字符串

# ============ 扩展思考 ============
# 1. 有没有更简洁的输出方式?
# 2. 如何让输出格式更美观?
# 3. 如何处理用户输入?

# 定义个人信息
student_name = "李明"
student_age = 22
student_city = "上海"
is_student = True

# 方式1: 分开输出
print("姓名:", student_name)
print("年龄:", student_age)
print("城市:", student_city)
print("在校生:", is_student)

print("-" * 40)  # 打印分隔线

# 方式2: 字符串拼接(需要类型转换)
info = "姓名:" + student_name + ", 年龄:" + str(student_age) + ", 城市:" + student_city
print(info)

print("-" * 40)

# 方式3: f-string格式化(最推荐，Python 3.6+)
info_formatted = f"姓名:{student_name}, 年龄:{student_age}, 城市:{student_city}, 在校生:{is_student}"
print(info_formatted)


