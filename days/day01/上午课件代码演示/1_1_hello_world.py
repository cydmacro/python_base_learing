print("="*50)

# 挑战题1: 算式输出
a = 2
b = 2
result = a + b
print(f"{a} + {b} = {result}")

# 挑战题2: 交换变量值
x = 1
y = 2
print(f"交换前: x={x}, y={y}")
# 方法1: 使用临时变量
temp = x
x = y
y = temp
print(f"交换后: x={x}, y={y}")

# 方法2: Python特有的优雅方式
x, y = 1, 2
print(f"交换前: x={x}, y={y}")
x, y = y, x  # 同时赋值
print(f"交换后: x={x}, y={y}")

